// WAP to find sum of 2 Numbers
#include<stdio.h>

void main(){
    int a, b, c, d, sum, diff, product, div ;
    a = 10;
    b = 50;
    c = 50;
    d = 30;
    sum = a+b;
    diff = c-d;
    product = a*d;
    div = c/a;
    printf("%d + %d = %d\n",a,b,sum);
    printf("%d - %d = %d\n",c, d, diff);
    printf("%d * %d = %d\n",a, d, product);
    printf("%d / %d = %d\n",c, a, div);
    
}
